      <p><span class="dropcap">I</span>n its previous two editions KIIT International MUN has reached heights which very few MUN Conferences around the world have achieved. In its first edition as the KIIT Model United Nations it hosted just a little over 500 delegates and members of the international press across  5 committees. The conference was graced among others by by His Excellency Dr. J. N. Mishra, Ambassador of India to Portugal & Dr. Deepa Prasad, State Programme Officer, UNFPA, Odisha The closing ceremony was attended by Barrister Ms. Linda Hunter & Prof. J. Martin Hunter, Barrister, Essex Court Chambers. Delegates from institutions across the country ranging from the IITs to the various national law universities took steps towards becoming the leaders of tomorrow. If their messages at the end of the Social Night were to be believed we gave some of them the “best time of their college loves”; which looking back, was just the encouragement we needed for the 2014 edition.</p>	
	  <p>With goals set high the now 80 member secretariat set off 6 months prior to the event. This time the event was rechristened the KIIT International MUN 2014 due to presence of Delegates from the countries of Kyrgyzstan, Kazakhstan, Nepal, Bangladesh, etc. The event saw a record participation of 1235 delegates and members of the press across 10 committees and ended as one of the World’s largest Model UNs in terms of delegate count. It was graced among others by UNIC Director, Ms. Kiran Mehra Kerpelmann, who apart from speaking in the inaugural ceremony, also hosted a film festival and opened a UN Reading Room in Kalinga Institute of Social Sciences.</p>
	  <p>The icing in the cake for the delegates was the chance to interact with Padmashri Rajat Sharma, the Founder Editor-In-Chief of India TV who regaled the audience with his life story and quips on the Indian political scenario! </p>
	  <p>The three days also saw delegates from various corners of the world socializing and networking across the three social events planned after committees ended each day. If the testimonials that poured in are to be believed, this time, we gave the delegates “ the best time of our lives”</p>
	  <p>We only hope to take it forward this year and hope you join us!</p>

