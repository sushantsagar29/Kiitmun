      <p><span class="dropcap">K</span>alinga Institute of Social Sciences (KISS) is the humane face of KIIT. It is the world’s largest school for tribal children and provides education from KG to PG completely free of cost and is also the home of 20,000 tribal children. This slice of heaven is not created by a man who has enough to donate nor funded by any agency; government, non-government or corporate but by an individual who had lost his father at the tender age of four and began a life ridden with abject poverty and had to endure many social and economic challenges himself. He is none other than Dr. Achyuta Samanta who nurtured a dream to eradicate the sufferings of the socially challenged tribal children from the country.</p>
	  <p>Kalinga Institute of Social Sciences (KISS) started with just 125 tribal children way back in 1993 has grown into the largest residential institute in the world for 20,000 tribal children.  It is a total transformation of these children and indeed a revolutionary endeavour aimed at empowering the poorest of the poor and the underprivileged tribal children through holistic education. </p>
