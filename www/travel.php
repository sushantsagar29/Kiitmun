    <p>Bhubaneswar is well connected to all parts of the country through a good network of transport. Flights, railways and buses ensure hassle free travel for the international and out station delegates.</p>
	<h1>FOR INTERNATIONAL DELEGATES:</h1>
	<p>Our team is ready to provide travel assistance and well believing in the Sanskrit phase “ Atithi Devo Bhava”, which means, a guest to our country is equivalent to god; you can get in touch with the Secretary General directly at secretarygeneral@kiitmun.org or call on 0091-750487807. Here is a quick guide:</p>
	<h5>A: From Your Country’s to India :</h5>
	<p>There are no direct flights from other countries to the Bhubaneswar Biju Patnaik International Airport except very few SAARC countries. In which case we would encourage you to book your flight tickets to New Delhi (Indira Gandhi International Airport) or to the city of Kolkata (Netaji Subash Chandra Bose International Airport) </p>

	<h5>B: From NEW DELHI/KOLKATA to BHUBANESWAR:</h5>

	<p>Indira Gandhi International Airport, New Delhi and Netaji Subash Chandra Bose International Airport, Kolkata are both well connected to Biju Patnaik International Airport, Bhubaneswar through various low cost domestic airlines such as Spice Jet, Go Air and Indigo airlines and other national carriers like Air India, Jet Airways, etc. You can log onto www.makemytrip.com for a quick view of your options! </p>
	<p>It is also well connected through comfortable superfast trains namely the Rajdhani Express from New Delhi and East Coast Express, Shatabdi Express, Dhauli Express, Puri-Howrah Express, Jagannath Express, Garibrath, etc from Kolkata. There is a reserved International Tourist quota for immediate reservation of tickets for the international passengers travelling to any part of India. You can log on to www.irctc.in for booking, assistance and queries regarding travel by rail. The International Tourist Bureaus are located at Kolkata (Howrah) and New Delhi railway stations which function from 08:00hrs to 20:00hrs from Monday to Saturday and on Sunday from 08:00hrs to 14:00hrs. These provide assistance to the foreign tourists regarding enquiries, booking and reservation. There are also Volvo Bus Services from Kolkata called the Royal Cruiser Bus Service, tickets for which and assistance can be obtained from www.redbus.in</p>
	<p>Airport and/or station pick up will be arranged once you send us your travel itinerary.</p>
	<h>FOR NATIONAL DELEGATES:</h1>

	<p>Delegates travelling from cities like Kolkata, Delhi, Mumbai, Hyderabad, Bangalore, Chennai, Ahmadabad to Biju Patnaik International Airport, Bhubaneswar by availing any of the following domestic flights: Air India, Go Air, Jet Airways, Indigo Airlines.</p>
	<p>Railway Facilities can also be availed to reach Bhubaneswar. Trains from Hyderabad include Bhubaneswar-Pune Express, Falaknuma Express and Yashwantpur Express. Coromandel Express connects Chennai to Bhubaneswar. Gitanjali Express and Duronto Express connect Mumbai and Bangalore to Bhubaneswar respectively. Numerous trains from Kolkata exist including the Shatabdi Express, Jaganath Express, etc.</p>
	<p>Reaching KIIT University after arriving in Bhubaneswar is easy with KIIT being a local landmark, easily connected by city buses and local for-hire autos. Radio Taxi facilities are also available. </p>

	 	 
