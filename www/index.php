<b>Dr. Achyuta Samanta</b> is an outstanding visionary and revolutionary who changed the face of education in his native state, Odisha, which is also one of the most backward states in India. The dismal poverty that he grew up in was his resolute force which geared him towards transforming the educational scenario in Odisha.  Dr. Samanta’s sole aim throughout his life has been to ensure accessibility to quality education for the deprived children of the society. 
</p>
<p style = "text-align:justify;">
Today, KIIT is one the largest multi-disciplinary universities, providing quality world class education in more than 100 courses and catering to more than 25000 students from India and 22 other countries. Dr. Samanta’s greatness lies in his selflessness and his indomitable spirit to uplift children from intergenerational cycles of poverty by providing them with holistic education. Instead of looking at personal gains, he aimed at channelizing the profits from Kalinga Institute of Industrial Technology (KIIT) group of institutes towards education of deprived children. With this goal, he started the <b>Kalinga Institute of Social Sciences (KISS)</b>, a residential institute in 1993 to empower and enable socially deprived indigenous children by providing them with free holistic education combined with other basic amenities. As a model institution in the country meant for the indigenous population (covering 62 tribes including 13 primitive tribes), KISS provides education (from class 1 to Post graduation), accommodation, food, health care, study material, clothing, vocational training (farm and non-farm sector), games and sports, computer education to the students for free. Starting with just 125 children, KISS today houses 25,000 indigenous and marginalized children from the most backward parts of Odisha and its neighbouring states, who are nurtured towards excellence and sustainable development. Dr. Achyuta Samanta is an educationist, philanthrophist, and social entrepreneur par excellence. He can be seen as the harbinger of ‘coalition of conscience’ – an innovative sustainable model to be emulated adapting to socio-cultural economic milieu with fruits of liberalisation percolating to every strata and ‘diversifying of talent pool’ thereby contributing to nation building.
</p>
