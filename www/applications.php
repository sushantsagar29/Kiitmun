      <h1>For Delegates of Indian Origin</h1>
      <p>1.	Apply as a delegate or press member from the forms under the ‘Applications’ section of our website.
<br>2.	Once you apply you will get an auto-generated reply confirming your application being received. Check your spam folder in case the email does not land up in your inbox. If you still cannot find it, drop an email to webteam@kiitmun.org citing your problem.
<br>3.	Once we receive your application we will allot you a country and committee or a press position if applying as press member. There will be three rounds of such allotments.
<br>4.	First Round Allotments will come out on 25th August ; Second Round Allotments will come out on 20th September and the third and final round will come out on 15th October, 2015.
<br>5.	You will be allotted in any of the above rounds based on the time and date at which we receive your application.
<br>6.	After you have received your allotment you shall be also receiving an email with the payment procedures.
<br>7.	All allotments are provisional and will be confirmed only after you pay your Delegate/ Press Fees.
<br>8.	The payment window for first round allotments shall be 26th August to 16th September ; for second round allotments between 21st September to 11th October and for the final third round from 15th October to 24th October, 2015.
<br>9.	The payment methods shall consist of bank account transfer; online payment and offline collection centre at Student Activity Center, KIIT University. Further details shall be sent by email in due course.
<br>10.	After you pay, you shall receive the confirmation by email and your participation in KIIT International MUN 2015 shall be confirmed.</p>
<br />
<h1>For Delegates of Foreign Origin</h1>
<p>1.	Apply as a delegate or press member from the forms under the ‘Applications’ section of our website.
<br>2.	Once you apply you will get an auto-generated reply confirming your application being received. Check your spam folder in case the email does not land up in your inbox. If you still cannot find it, drop an email to webteam@kiitmun.org citing your problem.
<br>3.	Once we receive your application we will allot you a country and committee or a press position if applying as press member. There will be three rounds of such allotments.
<br>4.	First Round Allotments for International Delegates will come out on 25th July ; Second Round Allotments will come out on 10th August ;  Third Round will come out on 25th August, 2015 ; Fourth Round will come out at 10th September, 2015 ; Fifth and final Round will come out at 20th September, 2015.
<br>5.	No applications will be considered after 19th September, 11:59 PM IST.
<br>6.	On receiving your allotment you are required to fill up a form for Visa Assistance letter that will be sent to you along with your allotment. 
<br>7.	Once you have your Visa Assistance letter you are required to send in your Indian Visa scanned copy/photo and travel tickets to any arrival point in India within the next 30 Days ( 1 Month).
<br>8.	Your allotment will be confirmed subject to us receiving the Visa and Travel Ticket documents.
<br>9.	In case you want to apply for a scholarship you can do so after checking out the ‘Scholarships & Grants’ section in the’ Information’ tab of our website.
<br>10.	In case you are not a scholarship sanctioned delegate you shall be required to pay the Registration and Accommodation Fees on arrival at KIIT University. The fee and accommodation details can be found in the FEES AND ACCOMODATION section of the ‘Information’ tab in our website.
<br>
</p>
      
